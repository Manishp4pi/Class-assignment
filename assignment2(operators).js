// Basic Maths Operator (+, -, %, /, *, power)
let firstFavNumber = 10;
let secondFavNumber = 7;

// addition +
console.log(firstFavNumber + secondFavNumber);

// substraction -
console.log(firstFavNumber - secondFavNumber);

// find remainder %
console.log(firstFavNumber % secondFavNumber);

// find quotient /
console.log(firstFavNumber / secondFavNumber);

// Power **
console.log(secondFavNumber ** 3);
