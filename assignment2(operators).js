// Basic Maths Operator (+, -, %, /, *, power)
let firstNumber = 10;
let secondNumber = 7;

// addition +
console.log(firstNumber + secondNumber);

// substraction -
console.log(firstNumber - secondNumber);

// find remainder %
console.log(firstNumber % secondNumber);

// find quotient /
console.log(firstNumber / secondNumber);

// Power **
console.log(secondNumber ** 3);
