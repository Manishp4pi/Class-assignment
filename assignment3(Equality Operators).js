// defining the variable first
let firstNumber = 500;
let secondNumber = 1220;

// loose equal ==
console.log(firstNumber == secondNumber);

// strict equal ===
console.log(firstNumber === secondNumber);

// loose non equal !=
console.log(firstNumber != secondNumber);

// strict non equal !==
console.log(firstNumber !== secondNumber);

// less than <
console.log(firstNumber < secondNumber);

// greater than >
console.log(firstNumber > secondNumber);

// less than or equal to <=
console.log(firstNumber <= secondNumber);

// greater than or equal to >=
console.log(firstNumber >= secondNumber);
